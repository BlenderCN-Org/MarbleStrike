namespace Render
{
	Math::Matrix44 ProjectionOrientation( const Math::Matrix44 &projectionMatrix )
	{
		Math::Matrix44 rotate90;
		if ( Database::Get()->GetIsLandscape() )
		{	
#if defined PLAT_WINDOWS_PHONE
			rotate90.Rotate( -90.0f, 0, 0, 1 );	
#endif
		}

		Math::Matrix44 newMatrix = rotate90 * projectionMatrix;
		return newMatrix;
	}
}