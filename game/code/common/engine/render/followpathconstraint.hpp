#ifndef FOLLOW_PATH_CONSTRAINT_HPP
#define FOLLOW_PATH_CONSTRAINT_HPP

//////////////////////////////////////////////////////
// INCLUDES
//////////////////////////////////////////////////////

#include "tinyxml2.h"

//////////////////////////////////////////////////////
// FORWARD DECLARATIONS
//////////////////////////////////////////////////////

class RenderObject;

//////////////////////////////////////////////////////
// CONSTANT
//////////////////////////////////////////////////////

//////////////////////////////////////////////////////
// CLASSES
//////////////////////////////////////////////////////

class FollowPathConstraint
{
public:

	FollowPathConstraint();
	~FollowPathConstraint();

	void Load( tinyxml2::XMLNode* node );

	const char* GetTargetName()
	{
		return mTargetName;
	}

	void SetTarget( RenderObject* target )
	{
		mTarget = target;
	}

	RenderObject* GetTarget()
	{
		return mTarget;
	}

protected:

	char mTargetName[64];
	RenderObject* mTarget;
};

#endif