	Triangle::Triangle()
	{
	}

	//===========================================================================

	Triangle::Triangle( Math::Vector3& v0, Math::Vector3& v1, Math::Vector3& v2 )
	{
		mVertex[0] = v0;
		mVertex[1] = v1;
		mVertex[2] = v2;

		mNormal = Math::MakeNormal( v0, v1, v2 );
	}

