#ifndef COMMAND_HPP
#define COMMAND_HPP

//////////////////////////////////////////////////////
// INCLUDES
//////////////////////////////////////////////////////

//////////////////////////////////////////////////////
// FORWARD DECLARATION
//////////////////////////////////////////////////////

class GuiEditorApp;

//////////////////////////////////////////////////////
// CONSTANTS
//////////////////////////////////////////////////////

enum CommandType
{
	COMMAND_MOVE,
	COMMAND_SAVE_STATE,
};

//////////////////////////////////////////////////////
// CLASSES
//////////////////////////////////////////////////////

class Command
{
public:
	Command( GuiEditorApp* guiEditorApp, CommandType type );
	virtual ~Command();

	virtual void Execute() = 0;
	virtual void Undo() = 0;

	CommandType GetCommandType()
	{
		return mCommandType;
	}

protected:

	GuiEditorApp* mGuiEditorApp;
	CommandType mCommandType;
};

#endif